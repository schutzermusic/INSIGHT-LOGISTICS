/**
 * Generate AI insights from simulation data
 * @param {Array} simulations
 * @param {Array} collaborators
 * @returns {Array} Insight objects
 */
export function generateDashboardInsights(simulations, collaborators) {
  const insights = [];

  if (simulations.length === 0) {
    insights.push({ type: 'info', message: 'Crie simulacoes para receber insights automaticos de otimizacao.' });
    return insights;
  }

  // Mode that generated most savings
  const modeDistribution = getTravelModeDistribution(simulations);
  if (modeDistribution.length > 0) {
    const topMode = modeDistribution[0];
    insights.push({
      type: 'info',
      message: `${topMode.name} e o modal mais utilizado (${topMode.percentage}% das analises).`,
    });
  }

  // Most expensive destinations
  const topDests = getCostByDestination(simulations, 3);
  if (topDests.length > 0) {
    insights.push({
      type: 'warning',
      message: `Destino mais caro: ${topDests[0].fullName} com custo medio de ${formatCurrency(topDests[0].avgCost)}.`,
    });
  }

  // AI savings
  const savings = getSavingsGeneratedByAI(simulations);
  if (savings.estimatedSavings > 0) {
    insights.push({
      type: 'success',
      message: `Analises AI geram economia media de ${formatCurrency(savings.estimatedSavings)} por mobilizacao (${savings.savingsPercentage}%).`,
    });
  }

  // Average travel time warning
  const avgTime = simulations.reduce((s, sim) => s + (sim.resumo?.horasTransito || 0), 0) / simulations.length;
  if (avgTime > 10) {
    insights.push({
      type: 'warning',
      message: `Tempo medio de transito de ${avgTime.toFixed(1)}h — considere modais mais rapidos para reduzir horas improdutivas.`,
    });
  }

  // AI usage suggestion
  const aiPercentage = simulations.filter(s => s.type === 'ai-analysis').length / simulations.length;
  if (aiPercentage < 0.3) {
    insights.push({
      type: 'suggestion',
      message: 'Apenas ' + Math.round(aiPercentage * 100) + '% das analises usam AI. Aumente o uso da Inteligencia de Rotas para otimizar custos.',
    });
  }

  return insights;
}

// Helpers

function getSimulationsToday(simulations) {
  const today = new Date().toDateString();
  return simulations.filter(s => s.createdAt && new Date(s.createdAt).toDateString() === today).length;
}

function normalizeModal(modal) {
  if (!modal) return 'Outro';
  if (modal.includes('nibus') || modal.includes('Onibus')) return 'Onibus';
  if (modal.includes('reo') || modal.includes('Aereo')) return 'Aereo';
  if (modal.includes('culo') || modal.includes('Veiculo') || modal.includes('Frota')) return 'Veiculo';
  return modal;
}

function avg(arr) {
  return arr.length > 0 ? arr.reduce((a, b) => a + b, 0) / arr.length : 0;
}

function pct(val, total) {
  return total > 0 ? Math.round((val / total) * 100) : 0;
}
